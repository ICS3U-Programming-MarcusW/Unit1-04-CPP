// This program calculates area and perimeter of a rectangle in c++

#include <cmath>
#include <iostream>

int main() {
    std::cout << "Area and perimeter of a rectangle - " << std::endl;
    std::cout << "If a rectangle has the dimensions:" << std::endl;
    std::cout << "7cm x 13cm" << std::endl;
    std::cout << " " << std::endl;
    std::cout << "The area is: " << (7 * 13) << "cm^2" << std::endl;
    std::cout << "The perimeter is: " << (2 * (7 + 13)) << "cm" << std::endl;
}

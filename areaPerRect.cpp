// This program calculates area and perimeter of a rectangle in c++

#include <cmath>
#include <iostream>

int main() {
    std::cout << "Area and perimeter of a rectangle - " << std::endl;
    std::cout << "If a rectangle has the dimensions:" << std::endl;
    std::cout << "17cm x 9cm" << std::endl;
    std::cout << " " << std::endl;
    std::cout << "The area is: " << (17 * 9) << "cm^2" << std::endl;
    std::cout << "The perimeter is: " << (2 * (17 + 9)) << "cm" << std::endl;
}
